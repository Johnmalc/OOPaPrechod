public class SchleifeGegenlaeufig {
  
  public static void main(String[] args) {
    
    byte max = 10;
    
    for (byte i = max;i >= 0 ;i-- ) {
      System.out.println(i + "\t" + (max-i));
    } // end of for
    
    
    
  } // end of main

} // end of class SchleifeGegenlaeufig
