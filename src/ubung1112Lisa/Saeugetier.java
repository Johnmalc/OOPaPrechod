package ubung1112Lisa;

public class Saeugetier {
	// Variablen

	String name;
	int alter;
	double gewicht;

	public Saeugetier() {

	}

	public Saeugetier(String namex, int alterx, double gewichtx) {
		name = namex;
		alter = alterx;
		gewicht = gewichtx;
	}

	public String getName() {
		return name;
	}

	public int getAlter() {
		return alter;
	}

	public double getGewicht() {
		return gewicht;
	}

} // end of class Saeugetier
