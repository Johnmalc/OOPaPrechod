public class SchleifeMathQuadrat {

	public static void main(String[] args) {

		byte max = 15;

		for (short zahl = 1; zahl <= max; zahl++) {
			System.out.println(zahl + " x " + zahl + " = " + zahl * zahl);
		} // end of for

	} // end of main

} // end of class MathQuadrat
